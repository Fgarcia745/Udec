class Incremento {//clase principal
    public void incremento() {
      int x=3;//declara una variable entera 'x' asignada con tres
      int valor = ++x * 10;//declara una varible 'valor' asignada con x incrementada en uno y multiplicada por 10 
        System. out.println(valor);//imprime 40
        System.out.println(x);//imprime 4
    }
}